package com.ye.balance.balanceproject.security;

import com.ye.balance.balanceproject.model.repo.UserRepo;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.authority.AuthorityUtils;
import org.springframework.security.core.userdetails.User;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;



@Service
public class AppUserDetailsService implements UserDetailsService {

    @Autowired
    private UserRepo userRepo;

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        
        return userRepo.findOneByLoginId(username).map(user -> User
                .withUsername(username)
                        .password(user.getPassword())
                        .authorities(AuthorityUtils.createAuthorityList(user.getRole().name()))
                        .disabled(!user.isActive())
                        .accountExpired(!user.isActive())
                        .build())
                .orElseThrow( () -> new UsernameNotFoundException("There is no user with login id %s.".formatted(username)));

    }
}
